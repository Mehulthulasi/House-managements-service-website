<?php
ob_start(); // Start output buffering
session_start();

// Database configuration
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "house";

// Create connection
$conn = new mysqli($servername, $username, $password, $dbname);

// Check connection
if ($conn->connect_error) {
    die("Connection failed: " . $conn->connect_error);
}

// Handle login and registration logic
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    if (isset($_POST['login'])) {
        // Check if the login form is submitted
        $email = $_POST['email'];
        $password = $_POST['password'];

        $sql = "SELECT * FROM users1 WHERE email = ?";
        $stmt = $conn->prepare($sql);
        $stmt->bind_param("s", $email);

        if ($stmt->execute()) {
            $result = $stmt->get_result();
            if ($result->num_rows > 0) {
                $row = $result->fetch_assoc();
                if ($password === $row['password']) {
                    // Check the user's role
                    if ($row['role'] == 'admin') {
                        // Redirect the admin after successful login
                        echo '<script>window.location.href = "admin/index_admin.html";</script>';
                        exit();
                    } elseif ($row['role'] == 'user') {
                        // Redirect the regular user after successful login
                        echo '<script>window.location.href = "index.html";</script>';
                        exit();
                    }
                } else {
                    echo "Invalid email or password";
                }
            } else {
                echo "Invalid email or password";
            }
        } else {
            echo "Error: " . $stmt->error;
        }

        $stmt->close();
    } elseif (isset($_POST['signup'])) {
        // Check if the registration form is submitted
        $username = $_POST['username'];
        $email = $_POST['email'];
        $password = $_POST['password'];

        // Check if the email is for an admin
        if ($email == 'admin.in@gmail.com') {
            $role = 'admin';
        } else {
            $role = 'user';
        }

        $sql = "INSERT INTO users1 (username, email, password, role) VALUES (?, ?, ?, ?)";
        $stmt = $conn->prepare($sql);
        $stmt->bind_param("ssss", $username, $email, $password, $role);

        if ($stmt->execute()) {
            echo "Registration successful. You can now log in.";
        } else {
            echo "Error: " . $stmt->error;
        }

        $stmt->close();
    }

    // Close the database connection
    $conn->close();
}
ob_end_flush(); // Flush the output buffer and turn off output buffering
?>




